// 1. Double a number
function double(n) {
    return n * 2;
}

// 2. Square a number
function square(n) {
    return n * n;
}

// 3. Check if a number is even
function isEven(n) {
    return n % 2 === 0;
}

// 4. Check if a number is odd
function isOdd(n) {
    return n % 2 !== 0;
}

// 5. Multiply two numbers
function multiply(a, b) {
    return a * b;
}

// 6. Subtract two numbers
function subtract(a, b) {
    return a - b;
}
